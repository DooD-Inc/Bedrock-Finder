public class Vec2i
{
    public Vec2i(int x, int z)
    {
        X = x;
        Z = z;
    }
    public int X { get; set; }
    public int Z { get; set; }
}
public class Vec2l
{
    public Vec2l(long x, long z)
    {
        X = x;
        Z = z;
    }
    public long X { get; set; }
    public long Z { get; set; }
}